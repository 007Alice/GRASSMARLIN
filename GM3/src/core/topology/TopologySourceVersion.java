package core.topology;

/**
 * Contains version information fields
 */
public class TopologySourceVersion {
    
    /**
     * The Software version.
     */
    public String version;
    /**
     * Usually the software-image name.
     */
    public String software;
    /** 
     * Usually a hardware model number.
     */
    public String model;
    /**
     * Serial number.
     */
    public String serial;
    
}
